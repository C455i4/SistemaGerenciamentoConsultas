     public void preencherTabela(){
         consultaDAO cdao = new consultaDAO();
         List<Consulta> lista = cdao.ListaTodas();
         DefaultTableModel tabela = (DefaultTableModel)jTableconsulta.getModel();
         tabela.setRowCount(0);
         for(Consulta c : lista){
             tabela.addRow(new Object[]{c.getIdconsulta(), c.getDataatend(), c.getHorario(), c.getMedico().getNome(), c.getPaciente().getNome()});
         }
     }
     public void preencherTabela(String datapesq){
         consultaDAO cdao = new consultaDAO();
         List<Consulta> lista = cdao.buscaData(datapesq);
         DefaultTableModel tabela = (DefaultTableModel)jTableconsulta.getModel();
         tabela.setRowCount(0);
         for(Consulta c : lista){
             tabela.addRow(new Object[]{c.getIdconsulta(), c.getDataatend(), c.getHorario(), c.getMedico().getNome(), c.getPaciente().getNome()});
         }
     }
     
