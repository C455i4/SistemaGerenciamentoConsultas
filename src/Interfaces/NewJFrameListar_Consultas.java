    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTableconsulta.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this,"Slelecione uma linha da tabela!");
            
        }
        else if(JOptionPane.showConfirmDialog(this,"Tem certeza que deseja excluir este paciente?")==JOptionPane.YES_OPTION){
            id=(int)jTableconsulta.getModel().getValueAt(jTableconsulta.getSelectedRow(), 0);
            consultaDAO.excluir(id);
             preencherTabela();
             
        }
    }//GEN-LAST:event_jButton1ActionPerformed

